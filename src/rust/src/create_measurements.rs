/// Fast LCG pseudo-random number generator.
struct Lcg(u64);
impl Lcg {
    fn new(seed: u64) -> Self { Lcg(seed ^ 0xdeadbeef_cafebabe) }
    #[inline]
    fn next_f32(&mut self) -> f32 {
        self.0 = self.0
            .wrapping_mul(6364136223846793005)
            .wrapping_add(1442695040888963407);
        ((self.0 >> 40) as f32) / (1u32 << 24) as f32
    }
}

fn main() -> io::Result<()> {
    let mut args = env::args().skip(1);
    let count: u64 = args.next()
        .unwrap_or_else(|| "1000000000".into())
        .parse()
        .expect("count must be a positive integer");
    let out_path = args.next().unwrap_or_else(|| "measurements.txt".into());

    eprintln!("Generating {} rows -> {}", count, out_path);

    let file = File::create(&out_path)?;
    let mut out = BufWriter::with_capacity(1 << 20, file);

    let n = STATIONS.len();
    // Fixed: 0x1b4c is a valid hex literal (was 0x1brc which is not)
    let mut rng = Lcg::new(count ^ 0x1b4c_u64);

    for i in 0..count {
        let idx = (i as usize) % n;
        let (name, lo, hi) = STATIONS[idx];
        let t = lo + rng.next_f32() * (hi - lo);
        let tenths = (t * 10.0).round() as i32;
        let sign   = if tenths < 0 { "-" } else { "" };
        let abs    = tenths.unsigned_abs();
        write!(out, "{};{}{}.{}\n", name, sign, abs / 10, abs % 10)?;

        if i % 50_000_000 == 0 && i > 0 {
            eprintln!("  {} M rows written...", i / 1_000_000);
        }
    }

    eprintln!("Done.");
    Ok(())
}
